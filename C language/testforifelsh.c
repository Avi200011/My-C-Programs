#include <stdio.h>
int main()
{
    int reward;
    printf("Enter your pass reward value\n");
    scanf("%d", &reward);
    printf("Your reward value is %d\n", reward);
    if (reward = 45)
    {
        printf("You passed in math and science both\n");
    }
    else if (reward = 15)
    {
        printf("You pass in math only\n");
    }
    else
    {
        printf("You pass only science");
    }

    return 0;
}